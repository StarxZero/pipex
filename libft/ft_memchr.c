#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*cs;

	i = 0;
	cs = (char *)s;
	while (i < n)
	{
		if (cs[i] == (char)c)
			break ;
		i++;
	}
	if (i == n)
		return (0);
	return (&cs[i]);
}
